package buclesfor;

public class MiBucleFor {

		public static void main(String[] args) {
			// primero se hace la inicialización solo una vez
			//luego se ejecutan las instrucciones que hay dentro de las llaves
			// luego se hace el incremento
			// por ultimo se evalua la condición
			for (int i =1;i<=10;i++) {
				System.out.println(i);
			}
		}
}
